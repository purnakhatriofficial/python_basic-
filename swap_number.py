# 8. Write a program to swap two numbers.

first_number = int(input("Enter the first number : "))

last_number = int(input("Enter the second number :"))

#Before swapping
print(f"Before Swapping : {first_number}, {last_number}")


#After Swiping 


first_number, last_number = last_number, first_number


print(f"After swiping : {first_number} ,{last_number} ")

